
public class bitwise {
    public static void main(String[] args) {
        int a = 10;
        int b = -20;
        //int c= a&b;
        //int c= a|b;
        //and operator
        System.out.println(a&b);
        //or operator
        System.out.println(a|b);
        //exor operator
        System.out.println(a^b);
        //not operator
        System.out.println(~a);
        // left shift
        System.out.println(a<<2);
        //right shift
        System.out.println(a>>5);
    }
}
